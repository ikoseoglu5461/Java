package com.class07;

public class OddNumbers {

	public static void main(String[] args) {
		/*
		 * sprint only odd numbers from 1to 20
		 * 
		 */

		int num = 1;

		while (num < 20) {
			
			System.out.println(" I am inside while loop");

			if (num % 2 != 0) {
				System.out.print(num);
			}
			num++;
		}
	//print only even number from 10 to 100 
		
		
		
	}
	
}
